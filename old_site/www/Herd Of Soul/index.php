    <img src="images/loca.png" width="20" height="20"></p> En février 2014, la chanteuse Elbi s’entoure d’une horde de petits cerfs rencontrés dans la forêt parisiennes et crée elbi’s HERD OF SOUL (en français : La horde d’âmes d’Elbi, ou Elbi à entendu parlé de Soul). </p></br>
En parallèle à son projet de Soul Electronique en pleine floraison (Le mouv’, Les Inrocks, …), Elbi donne essence à HERD OF SOUL, détonnant mix entre Soul, Jazz, Hip-Hop électronique. Ce groupe naissant ne va pas sans faire penser aux renommés et innovants Hiatus Kayote ou Robert Glasper.</p></br> La voix Amy Winehousienne et les textes tantôt noirs, tantôt amuseurs d’Elbi trouvent parfaitement leur place dans cette musique construite autour de ses envies. 
“Je rêvais de me concentrer sur la scène, sur le chant et sur l’énergie donnée au public. En tant que groupe, on possède un potentiel émotionnel fort et on a beaucoup plus de puissance que des boites à rythme et des logiciels : il y a tellement plus de possibilités d’interaction! C’est ce qui me manquait dans mon projet solo. J’ai trouvé à Paris une équipe innovatrice, des personnes humainement extraordinaires et j’ose espérer que ça se ressent dans notre musique.</p></br> “
Des arrangements maitrisés, un univers bien marqué et une énergie positive à toute épreuve, la horde à enregistré un EP Live au Kramus Deluxe Studio, qui sortira en Décembre 2014 en téléchargement libre. La vidéo du premier single “Something”, est déjà disponible sur YouTube. </p></br> </br>

La horde :</p></br> </br>
Chant, Composition, Textes : Elbi</p></br>
Originaire d’Annecy, la chanteuse, rappeuse et compositrice et productrice Lucile BAUER commence le chant à l’âge de 11 ans, la guitare à 15 ans et crée ‘Elbi’, pseudonyme sous lequel elle compose ses premières chansons. Après de nombreuses années passées autour du globe afin de mener à bien ses études de Management (HEC, International Events), elle s’installer à Paris et sort son EP Troublesome Soul (L’Ame dérangeante) sort en auto-production au printemps 2014. L’EP fait une première apparition remarquée par la presse française.</p></br>
http://www.elbimusic.com/</p></br></br>
Basse et Composition : Nicolas Bauer</p></br>
Nicolas BAUER débute la basse à l'âge de 13 ans et étudie à l'IMFP, au CIM puis dans différents conservatoires parisiens où il obtient un DEM jazz. Bassiste, contrebassiste, compositeur et arrangeur, il travaille avec de nombreux artistes tels que Nawel BENKRAIEM, Julien DORE , JAMMIE ou CHRISTOPHE et possède son propre projet Nicolas Bauer Quintet. </p></br>
http://www.nicolasbauer.fr/</p></br></br>
Trompette, Synthétiseurs et Composition : Olivier Miconi</p></br>
Olivier MICONI étudie la trompette jazz au Conservatoire de Toulon puis au CNSM de Paris. Il se produit avec différents projets musicaux et Big Bands (Archie Shepp, XYWHY, ). Arrangeur, compositeur, clavieriste et DJ, Olivier développe aussi STRAWS, projet de musiques électronique.  
Batterie : Antonin Violot</p></br></br>
Originaire d'Annemasse (74), Antonin commence les tablas à 4 ans. Après 4 ans de piano, de chant, puis trois ans de percussions classiques au conservatoire, il se dirige vers la batterie à l’âge de 14 ans et étudie au CMDL de Paris. Il est aujourd’hui batteur et percussionniste au sein de formations variées (Holy Oysters, Pop Psychédélique / Marcus and thé Music, Hard Rock / LynX Trio,  Jazz / Adrien Gallo, pop, variété). </p></br>
Claviers : Yves Arques</p></br></br>
Issu du CRR de Chambéry (Jazz) puis le CRR de Paris, Yves ARQUES obtient son DEM à l’unanimité et entre à la Haute Ecole de Musique de Lausanne (Bachelor Jazz). Il étudie l’écriture classique et la direction de choeurs, joue dans diverses formations (Micromégas, Kevin Luchetti Kintet) et forme le Yves Arques Quintet et le duo Lemaire/Arques (Tricollectif).</p></br></br>
Scratchs : Mr Tos (Jesus Crise)</p></br>
DJ-platiniste et producteur, Mr Tos foule la scène depuis 1996. DJ dans Wide Open Cage ou UHT, producteur et DJ sur General Dub, Versus ou son propre projet, Jesus déterre samples méconnus et redonne vie aux oubliés. </p></br></br>
Guitare : Raoul Ribeiro</p></br></br>
Autodidacte, Raoul RIBEIRO commence la guitare à 6ans et étudie au conservatoire de Givors de 11 à 13 ans. Il obtient son DEM de musiques actuelles au conservatoire de Chambéry puis devient guitariste au sein de formations variées (Tempo Forte, ). Batteur, bassiste, compositeur et arrangeur, Raoul développe aussi son projet Raztitone, basé sur des boucles musicales en direct.</p></br></br>
